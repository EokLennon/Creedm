    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        String genero;
        if(rbtnMasculino.isSelected()){
            genero = "0";
        }
        else{
            if(rbtnFemenino.isSelected()){
                genero = "1";
            }
            else{
                genero = "-1";
            }
        }
        
        cPersona.modificarPersonaTotal(tablaPersonas.getValueAt(fila, 0).toString(), txtNombre.getText(), txtP_Apellido.getText(), txtS_Apellido.getText(), genero, txtFec_Nac.getText(), txtDireccion.getText(), comboColonia.getSelectedItem(), txtTel_Fijo.getText(), txtTel_Movil.getText(), txtMail.getText());
        tablaPersonas.setModel(cPersona.seleccionarUltimasPersonas());
        
        txtNombre.setText("");
        txtP_Apellido.setText("");
        txtS_Apellido.setText("");
        buttonGroup1.clearSelection();
        txtFec_Nac.setText("");
        txtEdad.setText("");
        txtDireccion.setText("");
        txtCod_Postal.setText("");
        comboColonia.removeAllItems();
        txtNSE.setText("");
        txtMunicipio.setText("");
        buttonGroup2.clearSelection();
        txtTel_Fijo.setText("");
        txtTel_Movil.setText("");
        txtMail.setText("");
        rbtnEdo_Mex.setEnabled(true);
        rbtnCd_Mex.setEnabled(true);
        btnGuardarCambios.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnGuardar.setEnabled(true);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

