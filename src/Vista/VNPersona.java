    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String genero;
        if(rbtnMasculino.isSelected()){
            genero = "0";
        }
        else{
            if(rbtnFemenino.isSelected()){
                genero = "1";
            }
            else{
                genero = "-1";
            }
        }
        cPersona.insertarPersona(txtNombre.getText(), txtP_Apellido.getText(), txtS_Apellido.getText(), genero, txtFec_Nac.getText(), txtDireccion.getText(), comboColonia.getSelectedItem(), txtTel_Fijo.getText(), txtTel_Movil.getText(), txtMail.getText());
        tablaPersonas.setModel(cPersona.seleccionarUltimasPersonas());
        
        txtNombre.setText("");
        txtP_Apellido.setText("");
        txtS_Apellido.setText("");
        buttonGroup1.clearSelection();
        txtFec_Nac.setText("");
        txtEdad.setText("");
        txtDireccion.setText("");
        txtCod_Postal.setText("");
        comboColonia.removeAllItems();
        txtNSE.setText("");
        txtMunicipio.setText("");
        buttonGroup2.clearSelection();
        txtTel_Fijo.setText("");
        txtTel_Movil.setText("");
        txtMail.setText("");
        rbtnEdo_Mex.setEnabled(true);
        rbtnCd_Mex.setEnabled(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

